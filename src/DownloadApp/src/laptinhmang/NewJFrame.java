    private Object[][] loadDataLS() {
        Object[][] data = new Object[listLS.size()][3];
        for (int i = 0; i < listLS.size(); i++) {
            data[i][0] = listLS.get(i).getDateTime();
            data[i][1] = listLS.get(i).getFileName();
            data[i][2] = listLS.get(i).getLinkFile();
        }
        return data;
    }

    private void loadData() {
        Object[] data = new Object[3];
        for (int i = 0; i < listLS.size(); i++) {
            data[0] = listLS.get(i).getDateTime();
            data[1] = listLS.get(i).getFileName();
            data[2] = listLS.get(i).getLinkFile();
            dfModel.addRow(data);
        }

    }

    private void saveDataLS() {
        ios.writeList(listLS);
    }

