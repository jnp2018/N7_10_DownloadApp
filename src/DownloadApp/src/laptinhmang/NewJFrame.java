    public void checkDownd(int sleep){ 
       if(controllDownload.getSpeed(sleep)==0&&(controllDownload.getDownloadStatus() == ControllDownload.DownladStart)){ 
           check++;
       }
       else{
           check =0;
       }
       if(check>5&&(controllDownload.getCurrent()<controllDownload.getSize())){ // tốc độ download = 0 và file download chưa hoàn thành sau 3 giây sẽ download lại
           controllDownload.start();
       }
    }
